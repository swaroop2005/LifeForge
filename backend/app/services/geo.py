def _normalize(name: str) -> str:
    """Normalize district name for fuzzy matching."""
    return name.lower().replace("-", " ").replace(".", "").replace("(", "").replace(")", "").strip()

_NORMALIZED_DISTRICTS = {_normalize(k): v for k, v in DISTRICT_CENTROIDS.items()}

def get_coords(state: str, district: str = None):
    """Return (lat, lng) for a state/district. Falls back to state centroid + hash jitter."""
    if district:
        # Exact match
        if district in DISTRICT_CENTROIDS:
            return DISTRICT_CENTROIDS[district]
        # Normalized match (handles hyphen/case/punctuation differences)
        norm = _normalize(district)
        if norm in _NORMALIZED_DISTRICTS:
            return _NORMALIZED_DISTRICTS[norm]

    # Fall back to state centroid + hash-based jitter for visual spread
    base = STATE_CENTROIDS.get(state)
    if not base:
        return (20.5937, 78.9629)  # India center

    lat, lng = base
    if district:
        # Consistent jitter per district (±0.4 degrees ≈ ±44km)
        h = int(hashlib.md5(f"{state}:{district}".encode()).hexdigest(), 16)
        lat += ((h % 1000) / 1000.0 - 0.5) * 0.8
        lng += (((h >> 10) % 1000) / 1000.0 - 0.5) * 0.8
    return (round(lat, 4), round(lng, 4))
